// Function that returns an array for sine wave generation


uint8_t *generateSineWave(uint16_t frequency){
	uint8_t *sineWave;
	//frequency = 1/sample * 44.1 kHz
	uint8_t sample_size = (uint8_t)setFrequency/frequency;
	sineWave = (uint8_t *)malloc(sample_size*sizeof(uint8_t));
	for(int i = 0; i < sample_size; i++){
		sineWave[i] = maxAmp * (arm_sin_f32((float)(2*PI*i)/sample_size) + 1);
	}
	return sineWave;
}

